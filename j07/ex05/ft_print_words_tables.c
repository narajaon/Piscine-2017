#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		ft_putchar(str[i]);
		i++;
	}
}

void	ft_print_words_tables(char **tab)
{
	int i;

	i = 0;
	while (tab[i + 1])
	{
		ft_putstr(tab[i]);
		if (!(tab[i]))
			break;
		else
			ft_putchar('\n');
		i++;
	}
}
